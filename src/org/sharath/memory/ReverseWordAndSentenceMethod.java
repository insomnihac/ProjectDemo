package org.sharath.memory;

public class ReverseWordAndSentenceMethod {

	public static void main(String[] args) {

		StringBuffer sbuf = new StringBuffer(" !!! AIDNI EVOL I ");

		StringBuilder sbil = new StringBuilder(" !!! AIDNI EVOL I ");

		System.out.println(" Reverse using String Buffer :  " + sbuf.reverse());

		System.out.println(" Reverse using String Builder :  " + sbil.reverse());

	}

}
