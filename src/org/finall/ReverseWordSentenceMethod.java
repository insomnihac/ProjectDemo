package org.finall;

public class ReverseWordSentenceMethod {

	public static void main(String[] args) {

		StringBuffer s1 = new StringBuffer("!!! aidnI evoL I");

		StringBuilder s2 = new StringBuilder(" I Love India !!!!");

		
		System.out.println( s1 + " Reversed Using String Buffer : " + s1.reverse());
		
		System.out.println( s2 + " Reversed Using String Builder : " + s1.reverse());
		
		
		
	}

}
